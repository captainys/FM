						MOV		AL,1	; ADDRESS 1
						OUT		DX,AL

						MOV		DX,TOWNSIO_VIDEO_OUT_DATA
						MOV		AL,29H	; Layer 1 16-color Palette Priority=Layer 1
						OUT		DX,AL
						CALL	RESETPALETTE16_PAGE

						MOV		DX,TOWNSIO_VIDEO_OUT_DATA
						MOV		AL,09H	; Layer 1 16-color Palette Priority=Layer 1
						OUT		DX,AL
						CALL	RESETPALETTE16_PAGE

						POP		AX
						POP		DX
						POP		SI
						RET
RESETPALETTE16			ENDP



