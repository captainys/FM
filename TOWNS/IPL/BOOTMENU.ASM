; Input
;   AL		Selection
;   CL		Number of menu items
DRAWARROW				PROC
						PUSH	DX

						PUSH	AX	; Save Selection

						MOV		AX,FIRST_ARROW_LOCATION
						XOR		CH,CH

@@:
						PUSH	AX
						PUSH	CX
						CALL	LOCATE31K
						MOV		SI,OFFSET ARROW_ERASE
						CALL	WRSTRING_WHITE_HIGH
						POP		CX
						POP		AX
						INC		AL
						LOOP	@b

						POP		CX	; Pop Selection in CL
						MOV		AX,FIRST_ARROW_LOCATION
						ADD		AL,CL
						CALL	LOCATE31K
						MOV		DH,0CH
						MOV		SI,OFFSET ARROW_DRAW
						CALL	WRSTRING_HIGH

						POP		DX
						RET

DRAWARROW				ENDP



TRYBOOT					PROC
						CMP		BYTE PTR CS:[BOOTMENU_SELECTION],BOOTMENU_ITEMNUM_RETURN_TO_MAINMENU
						JNE		SHORT @f
						RET

@@:
						CMP		BYTE PTR CS:[BOOTMENU_SELECTION],BOOTMENU_ITEMNUM_SCSI_CD
						JNE		TRYBOOT_NOT_FROM_SCSI_CD
						TEST	CS:[CONTROL_FLAGS],CONTROL_FLAG_PROMPT_CD_CHANGE
						JE		SHORT @f
						CALL	BOOTMENU_PROMPT_CD_CHANGE
@@:
						CALL	BOOT_FROM_SCSI_CD
						RET


TRYBOOT_NOT_FROM_SCSI_CD:
						XOR		DX,DX
						XOR		CX,CX

						CMP		BYTE PTR CS:[BOOTMENU_SELECTION],BOOTMENU_ITEMNUM_INTERNAL_CD
						JNE		SHORT @f

						; Boot from Internal CD
						MOV		AL,0C0H
						MOV		BX,0008H
						JMP		TRYBOOT_FROMDEVICE

@@:
						CMP		BYTE PTR CS:[BOOTMENU_SELECTION],BOOTMENU_ITEMNUM_SCSI_HDD_0
						JAE		SHORT @f

						; Boot from Floppy Disk
						MOV		AL,CS:[BOOTMENU_SELECTION]
						SUB		AL,BOOTMENU_ITEMNUM_FD0
						OR		AL,20H
						MOV		AH,3  ; Restore
						XOR		CH,CH
						DB		9AH		; CALLF FFFB:0014
						DW		0014H,0FFFBH


						MOV		BH,CS:[BOOTMENU_SELECTION]
						SUB		BH,BOOTMENU_ITEMNUM_FD0
						MOV		BL,02H

						MOV		AL,BH
						OR		AL,20H
						MOV		DL,1		; Floppy Disk Sector starts at 1.
						JMP		TRYBOOT_FROMDEVICE

@@:
						CMP		BYTE PTR CS:[BOOTMENU_SELECTION],BOOTMENU_ITEMNUM_PARTITION_0
						JAE		SHORT @f

						; Boot from a SCSI HDD
						MOV		BH,CS:[BOOTMENU_SELECTION]
						SUB		BH,BOOTMENU_ITEMNUM_SCSI_HDD_0
						MOV		BL,1

						MOV		AL,BH
						OR		AL,0B0H
						JMP		SHORT TRYBOOT_FROMDEVICE

@@:
						; Boot from Hard-Disk Partition
						CALL	SETUP_PARTITION_TABLE_B100_SEGMENT

						MOV		AL,CS:[BOOTMENU_SELECTION]
						SUB		AL,BOOTMENU_ITEMNUM_PARTITION_0
						MOV		AH,30H
						MUL		AH
						ADD		AX,20H
						MOV		SI,AX

						MOV		DX,WORD PTR CS:[SI+PARTITION_TABLE+2]	; LBA Low
						MOV		CL,BYTE PTR CS:[SI+PARTITION_TABLE+4]	; LBA High
						XOR		CH,CH

						MOV		BX,CS:[INCOMING_BX]
						AND		BH,7

						MOV		AL,BH
						OR		AL,0B0H

						JMP		SHORT TRYBOOT_FROMDEVICE
TRYBOOT					ENDP



; To tell Fujitsu's boot sector which partition it is booting from,
; partition table needs to be loaded from B100:0000, and
; boot-partition flag needs to be set according to the selected
; partition.
SETUP_PARTITION_TABLE_B100_SEGMENT	PROC
						PUSH	DS
						PUSH	ES


						MOV		AX,CS
						MOV		DS,AX
						MOV		AX,0B100H
						MOV		ES,AX

						MOV		SI,OFFSET PARTITION_TABLE
						XOR		DI,DI
						MOV		CX,512
						REP MOVSB


						MOV		DI,0020H
						MOV		CX,10
						XOR		AL,AL
@@:
						MOV		BYTE PTR ES:[DI],0	; Tentatively clear boot-partition flag
						ADD		DI,0030H
						LOOP	@b


						MOV		AL,CS:[BOOTMENU_SELECTION]
						SUB		AL,BOOTMENU_ITEMNUM_PARTITION_0
						MOV		AH,30H
						MUL		AH
						ADD		AX,20H
						MOV		DI,AX
						MOV		BYTE PTR ES:[DI],0FFH	; Boot-partition flag

						POP		ES
						POP		DS
						RET
SETUP_PARTITION_TABLE_B100_SEGMENT	ENDP



; Input
;   AL   	Boot Device Code for BIOS
;	CL|DX	Boot Sector as Specified by BIOS
;	BX		Boot Device ID
; 		 BL:Device Type   1:HD/CD  2:FD  8:CD
TRYBOOT_FROMDEVICE		PROC

						PUSH	DS
						PUSH	BX

						MOV		BX,0B000H
						MOV		DS,BX

						MOV		DWORD PTR DS:[0],0

						MOV		AH,05H	; Read Sector
						MOV		CH,0
						MOV		BX,4	; 4 sectors
						MOV		DI,0	; Offset=0

						; How can I write CALL 		FAR PTR 0FFFBH:0014H
						DB		9AH		; CALLF FFFB:0014
						DW		 0014H
						DW		0FFFBH

						MOV		EAX,DS:[0]
						POP		BX
						POP		DS
						JB		TRYBOOT_READERROR

						CMP		EAX,344C5049H	; "IPL4"
						JNE		TRYBOOT_NOT_IPL4

						MOV		AX,0FFFFH

						; Uncomment following DB and DWs and comment CALL SETUP_INTERCEPT_AND_GO
						; for simple boot without patching IPL.
						; How can I write CALL 		FAR PTR 0FFFBH:0014H
						;DB		9AH		; CALLF B000:0004
						;DW		 0004H
						;DW		0B000H
						CALL	SETUP_INTERCEPT_AND_GO

						PUSH	CS
						POP		DS

						RET

TRYBOOT_READERROR:
						TEXTLOCATION31K	ERRORMESSAGE_X,ERRORMESSAGE_Y
						MOV		SI,OFFSET READERROR_MESSAGE
						CALL	WRSTRING_WHITE_HIGH
						RET

TRYBOOT_NOT_IPL4:
						TEXTLOCATION31K	ERRORMESSAGE_X,ERRORMESSAGE_Y
						MOV		SI,OFFSET NOT_IPL4_MESSAGE
						CALL	WRSTRING_WHITE_HIGH
						RET


TRYBOOT_FROMDEVICE		ENDP
