BOOTMENU				PROC

						CALL	CLS_5BELOW_HIGH

						TEST	CS:[CONTROL_FLAGS],CONTROL_FLAG_DONT_REMEMBER_BOOT_CHOICE
						JNE		SHORT @f
						CALL	LOAD_OPTION
@@:

						CALL	BOOTMENU_SETUP

BOOTMENU_OUTER_LOOP:
						CALL	MENU_WAIT_PAD_RELEASE

						MOV		AX,DISPLACED_DEMOSPLASh_MESSAGE_LOCATION
						CALL	DEMOSPLASH_MESSAGE_MOVABLE

						MOV		AX,DISPLACED_DEMOSPLASh_MESSAGE_LOCATION
						ADD		AL,5
						CALL	HIROSAKI_MESSAGE_MOVABLE

						TEXTLOCATION31K	INSTRUCTION_X,INSTRUCTION_Y
						MOV		SI,OFFSET INSTRUCTION_MESSAGE
						CALL	WRSTRING_WHITE_HIGH

						MOV		SI,OFFSET MENU_EXPAND_BUF
						MOVZX	CX,CS:[NUM_BOOTMENU_OPTIONS]
						CALL	DRAWMENU2

						MOV		CL,CS:[NUM_BOOTMENU_OPTIONS]
						MOV		AL,CS:[BOOTMENU_SELECTION]
						CALL	DRAWARROW


						; Wait for PAD while count down for AUTOBOOT >>
BOOTMENU_PAD_LOOP:
						CMP		BYTE PTR CS:[AUTOBOOT],0
						JE		BOOTMENU_PAD_READ
						MOV		DX,IO_VSYNC
						IN		AL,DX
						AND		AL,1
						MOV		AH,CS:[AUTOBOOT_PREV_VSYNC]
						MOV		CS:[AUTOBOOT_PREV_VSYNC],AL
						XOR		AH,1
						AND		AH,AL
						JE		BOOTMENU_PAD_READ

						DEC		WORD PTR CS:[AUTOBOOT_COUNTER]
						MOV		AL,0FH	; Virtually Press A button
						CMP		WORD PTR CS:[AUTOBOOT_COUNTER],0
						JE		BOOTMENU_PAD_IS_IN

						MOV		AX,CS:[AUTOBOOT_COUNTER]
						MOV		DL,60
						DIV		DL
						AND		AH,AH
						JNE		BOOTMENU_PAD_READ

						CALL	ITOX8
						XCHG	AH,AL
						MOV		WORD PTR CS:[AUTOBOOT_NUMBER],AX

						PUSH	DX

						MOV		DH,0CH	; Green

						TEXTLOCATION31K	AUTOBOOT_MESSAGE_X,AUTOBOOT_MESSAGE_Y
						MOV		SI,OFFSET AUTOBOOT_MSG
						CALL	WRSTRING_HIGH

						MOV		AX,FIRST_ARROW_LOCATION
						ADD		AL,CS:[BOOTMENU_SELECTION]
						CALL	LOCATE31K
						MOV		SI,OFFSET AUTOBOOT_NUMBER
						MOV		DH,0CH
						CALL	WRSTRING_HIGH

						POP		DX

BOOTMENU_PAD_READ:
						CALL	READ_PADA
						CMP		AL,3FH
						JE		BOOTMENU_PAD_LOOP
						; Wait for PAD while count down for AUTOBOOT <<



BOOTMENU_PAD_IS_IN:
						MOV		BYTE PTR CS:[AUTOBOOT],0	; Once pad is moved, no more auto boot

						TEXTLOCATION31K	0,AUTOBOOT_MESSAGE_Y
						CALL	ERASE_LINE_HIGH

						MOV		AH,CS:[BOOTMENU_SELECTION]
						MOV		CH,CS:[NUM_BOOTMENU_OPTIONS]
						CALL	MOVE_ARROW_BY_PAD
						MOV		CS:[BOOTMENU_SELECTION],AH

						AND		AL,30H
						CMP		AL,30H
						JE		BOOTMENU_OUTER_LOOP

						CMP		BYTE PTR CS:[BOOTMENU_SELECTION],0
						JE		BACK_TO_MAIN_MENU

						TEST	CS:[CONTROL_FLAGS],CONTROL_FLAG_DONT_REMEMBER_BOOT_CHOICE
						JNE		SHORT @f
						CALL	SAVE_OPTION
@@:
						CALL	CLS_5BELOW_HIGH
						CALL	TRYBOOT
						CALL	CLS_5BELOW_HIGH

						JMP		BOOTMENU_OUTER_LOOP

BACK_TO_MAIN_MENU:
						RET

BOOTMENU				ENDP



; Input
;   DS:SI	Menu Top
;   CX		Number of Items
; Output
;   CX,SI   Destroyed
DRAWMENU2				PROC
						MOV		AX,FIRST_MENU_ITEM_LOCATION
DRAWMENU2_OUTER_LOOP:
						PUSH	CX
						PUSH	AX
						PUSH	SI
						CALL	LOCATE31K
						CALL	WRSTRING_WHITE_HIGH
						POP		SI

						CALL	SKIP_CSTR_DS_SI

						POP		AX
						POP		CX
						INC		AL
						LOOP	DRAWMENU2_OUTER_LOOP

						RET
DRAWMENU2				ENDP



; Input
;   DS:SI   C-String
; Output
;   DS:SI   Next C-String
SKIP_CSTR_DS_SI			PROC
						INC		SI
						CMP		BYTE PTR [SI],0
						JNE		SKIP_CSTR_DS_SI
						INC		SI
						RET
SKIP_CSTR_DS_SI			ENDP



BOOTMENU_SETUP			PROC

						PUSH	CS
						POP		ES

						MOV		SI,OFFSET BOOTMENU_ITEM_BUFFER
						MOV		DI,OFFSET MENU_EXPAND_BUF
						MOV		CX,BOOTMENU_ITEM_BUFFER_END-BOOTMENU_ITEM_BUFFER
						REP MOVSB

						MOV		CS:[NUM_BOOTMENU_OPTIONS],BOOTMENU_ITEMNUM_PARTITION_0

						TEST	CS:[CONTROL_FLAGS],CONTROL_FLAG_DONT_SCAN_PARTITION
						JNE		BOOTMENU_SETUP_DONE


						PUSH	DI

						MOV		BX,CS:[INCOMING_BX]

						MOV		AH,5
						MOV		AL,BH
						AND		AL, 07H
						OR		AL,0B0H

						XOR		CX,CX
						MOV		DX,1	; LBA=1 Partition Table

						MOV		BX,1

						MOV		DI,OFFSET PARTITION_TABLE

						; How can I write CALL 		FAR PTR 0FFFBH:0014H
						DB		9AH		; CALLF FFFB:0014
						DW		 0014H
						DW		0FFFBH

						MOV		SI,OFFSET PARTITION_TABLE+20H

						POP		DI


						XOR		CX,CX
BOOTMENU_SETUP_OUTER_LOOP:
						CMP		BYTE PTR CS:[SI+1],0
						JE		BOOTMENU_SETUP_DONE
						CMP		BYTE PTR CS:[SI+1],90H	; I don't know about 90H, but HD Boot Loader stops checking when it see 90H
						JE		BOOTMENU_SETUP_DONE

						PUSH	SI
						PUSH	CX

						MOV		SI,OFFSET BOOTMENU_ITEM_PARTITION
						MOV		CX,BOOTMENU_ITEM_PARTITION_END-BOOTMENU_ITEM_PARTITION
						REP		MOVSB

						POP		CX
						POP		SI
						PUSH	SI
						PUSH	CX

						ADD		CL,'0'
						MOV		[DI-3],CL

						MOV		CX,16
						ADD		SI,20H
						REP		MOVSB
						MOV		AL,']'
						STOSB
						XOR		AL,AL
						STOSB

						POP		CX
						POP		SI

						INC		CS:[NUM_BOOTMENU_OPTIONS]
						ADD		SI,030H
						CMP		CX,5
						JNE		BOOTMENU_SETUP_OUTER_LOOP

BOOTMENU_SETUP_DONE:

						MOV		CL,CS:[BOOTMENU_SELECTION]
						CMP		CL,CS:[NUM_BOOTMENU_OPTIONS]
						JAE		SHORT @f
						MOV		CS:[BOOTMENU_SELECTION],CL

@@:
						RET

BOOTMENU_SETUP			ENDP



